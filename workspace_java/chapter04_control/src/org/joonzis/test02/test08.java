package org.joonzis.test02;

public class test08 {
	public static void main(String[] args) {
//		Test01.java
//		Q. 10 ~ 1 역순으로 출력하기
		
		// for문
		
		for (int i = 10; i >= 1 ; i--) {
			System.out.println(i);
			
		}
		
		// while 문
		
		int i = 10;
		
		while (i >= 1) {
			
			System.out.println(i);
			i--;
			
		}
	}
}
